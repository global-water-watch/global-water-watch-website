import { capitalize } from '@/lib/primitive-helpers'

const formatTimeSeries = (timeSeries) => {
  const valueName = timeSeries[0]?.name?.split('_').map(capitalize).join(' ')

  // TODO: make sure this km2 comes from the backend again as an unit
  // const valueUnit = timeSeries[0]?.unit
  const valueUnit = 'km2'

  const data = timeSeries.map(({ t, value: valueInM2 }) => {
    const value = (valueInM2 / 1000000).toFixed(2)
    return [t, value]
  })

  return {
    xAxis: [
      {
        type: 'time',
        axisPointer: {
          label: {
            show: true,
          },
          handle: {
            show: true,
          },
        },
      },
    ],
    yAxis: [
      {
        name: `${valueName} (${valueUnit})`,
        type: 'value',
      },
    ],
    series: [
      {
        name: valueName,
        type: 'line',
        data,
      },
    ],
  }
}

const formatTimeSeriesByGeometry = (timeSeries) => {
  const series = []

  const valueName = timeSeries?.variable_name
    ?.split('_')
    .map(capitalize)
    .join(' ')

  const valueUnit = timeSeries?.variable_unit

  if (timeSeries.agg_type === 'sum') {
    series.push({
      name: 'Sum',
      type: 'line',
      areaStyle: {},
      data: timeSeries?.data?.map(({ t, value }) => {
        return [t, value]
      }),
    })
  }

  if (timeSeries.source_data) {
    Object.keys(timeSeries.source_data).forEach((key) => {
      series.push({
        name: `${valueName} (${key})`,
        type: 'line',
        data: timeSeries.source_data[key].map(({ t, value }) => {
          return [t, value]
        }),
      })
    })
  }

  return {
    xAxis: [{
      type: 'time',
      axisPointer: {
        label: {
          show: true,
        },
        handle: {
          show: true,
        },
      },
    }],
    yAxis: [
      {
        name: `${valueName} (${valueUnit})`,
        type: 'value',
      },
    ],
    series,
  }
}

